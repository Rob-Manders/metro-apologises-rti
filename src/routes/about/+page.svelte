<div class="container">
	<h2>About</h2>

	<p>Metro Apologises is an alternative live departure board for the Tyne and Wear Metro, designed to give quick and
		immediate access to the departure times you need.</p>

	<p>Metro Apologises is a third party service and is neither affiliated with nor endorsed by Nexus or the Tyne and
		Wear Metro.</p>

	<p>The name 'Metro Apologises' is taken from a common retort used on the <a
			href="https://www.reddit.com/r/NewcastleUponTyne">Newcastle-upon-Tyne subreddit</a>. I initially
		registered the domain as a bit of a joke, but decided to turn it into something useful.</p>

	<p>This site uses the <a href="https://github.com/danielgjackson/metro-rti">Metro Real Time Information API</a>,
		which is provided by Nexus and documented by Daniel G Jackson.</p>

	<p>This site does not use cookies. However, we do use local storage to store your saved stations. No other data is stored, and we do not share this data outside of this application.</p>

	<p>The source code for this site is available on <a
			href="https://github.com/Rob-Manders/metro-apologises-rti">GitHub</a> under the MIT license.</p>
</div>

